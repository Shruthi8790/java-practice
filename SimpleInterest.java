
public class SimpleInterest {

	public static void main(String[] args) {
        int p=20000;
        float r=2.5f;
        int t=1;// Time period (in years)
        float i;
        i=p*r*t/100;
        System.out.println("Principle Amount is:" +p);
        System.out.println("Annual Interest Rate is:" +r);
        System.out.println("Time is:" +t);
        System.out.println("Simple Interest is:" +i);
        
        
	}

}
